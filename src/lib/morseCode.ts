// Create reverse map (morse code to character)
export const morseToCharMap: Record<string, string> = Object.entries(
  morseCodeMap,
).reduce(
  (acc, [char, morse]) => {
    acc[morse] = char;
    return acc;
  },
  {} as Record<string, string>,
);
